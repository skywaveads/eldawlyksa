// Función para convertir fechas en árabe a formato ISO
function formatDateToISO(arabicDate: string): string {
  // Extraer el año al final de la cadena (asumiendo formato "DD nombre-mes YYYY")
  const yearMatch = arabicDate.match(/\d{4}$/);
  if (yearMatch) {
    const year = yearMatch[0];
    // Crear una fecha ISO válida con el año extraído (usando 1 de enero como fecha por defecto)
    return `${year}-01-01T00:00:00.000Z`;
  }
  // Si no se puede extraer el año, devolver una fecha por defecto
  return new Date().toISOString();
}

